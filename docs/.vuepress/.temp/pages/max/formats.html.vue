<h2 id="mo3" tabindex="-1"><a class="header-anchor" href="#mo3" aria-hidden="true">#</a> MO3</h2>
<p>支持无损压缩。</p>
<p>MO3 是一种文件格式，封装了几种“MOD”格式（IT，XM，S3M，MTM，MOD，MPTM）的功能，但有一个很大的区别：MP3 和 OGG 压缩样本。对于任何不喜欢有损编码的样本，还会合并无损编解码器，并且整个文件结构也经过压缩。
所有这些都使得在几乎相同的音质下大大减小文件大小成为可能。事实上，MO3 允许更高质量的合成，因为 MP3 / OGG 编码在处理 16 位样本时效果更好，因此没有理由再使用低质量的 8 位样本。</p>
