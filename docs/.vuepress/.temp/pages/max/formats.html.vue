<h2 id="ape-ape-monkey-s-audio" tabindex="-1"><a class="header-anchor" href="#ape-ape-monkey-s-audio" aria-hidden="true">#</a> APE(.ape) / Monkey's Audio</h2>
<p>Monkey's Audio 是一种快速简便的压缩数字音乐的方法。与 mp3，ogg 或 wma 等传统方法不同，这些方法会永久丢弃质量以节省空间，而 Monkey 的音频只能制作完美的，逐位复制的音乐。这意味着它听起来总是很完美 - 与原版完全相同。即使声音是完美的，它仍然节省了大量空间（可以将其视为增强的 Winzip™ 音乐）。另一件好事是，您始终可以将 Monkey 的音频文件解压缩回确切的原始文件。这样，您就不必重新复制 CD 收藏以切换格式，并且始终能够完美地重新创建原始音乐 CD 。</p>
