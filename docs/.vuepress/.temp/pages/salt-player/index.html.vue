<h3 id="软件技术" tabindex="-1"><a class="header-anchor" href="#软件技术" aria-hidden="true">#</a> 软件技术</h3>
<p>椒盐音乐最初版本来自 Dso Music ，经过近一年的技术迭代，使用了最新的 Jetpack Compose 技术并自研架构管理，保证了出色且生动的软件界面和较高的稳定性。数十万行级别的 Koltin / Java / C / C++ 代码共同完成了此应用。</p>
<h3 id="高维护力" tabindex="-1"><a class="header-anchor" href="#高维护力" aria-hidden="true">#</a> 高维护力</h3>
<p>在过去一年，椒盐音乐更新迭代了几百个版本。</p>
<h3 id="音频技术" tabindex="-1"><a class="header-anchor" href="#音频技术" aria-hidden="true">#</a> 音频技术</h3>
<p>椒盐音乐音频技术部分来自著名音频库（已获授权），并有使用了 FFmpeg 相关技术（部分代码公布于作者其他仓库）。</p>
<p>出色的音频技术为椒盐音乐带来了 AudioTrack、OpenSL ES、AAudio 输出。支持可变采样率音频等等。</p>
<p>格式支持：Mpeg 1 / 2 / 3、WAV、FLAC、APE、ALAC、AAC、DSD、WV、OGG 。</p>
<p>内置均衡器，支持参数均衡。</p>
